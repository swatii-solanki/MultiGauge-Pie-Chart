package com.techchallengepiechart.model;

public class MAbility {

    private String name;
    private Float totalMarksObtained;
    private Float outOf;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Float getTotalMarksObtained() {
        return totalMarksObtained;
    }

    public void setTotalMarksObtained(Float totalMarksObtained) {
        this.totalMarksObtained = totalMarksObtained;
    }

    public Float getOutOf() {
        return outOf;
    }

    public void setOutOf(Float outOf) {
        this.outOf = outOf;
    }
}
